/**
 * Various sizes for components
 *
 * 'xxs' - extra extra small
 * 'xs' - extra small
 * 's' - small
 * 'm' - medium (typically default)
 * 'l' - large
 * 'xl' - extra large
 * 'xxl' - extra extra large
 */

export type SizeM = 'm';

export type SizeS = SizeM | 's';

export type SizeL = SizeM | 'l';

export type SizeXS = SizeS | 'xs';

export type SizeXXS = SizeXS | 'xxs';

export type SizeXL = SizeL | 'xl';

export type SizeXXL = SizeXL | 'xxl';
